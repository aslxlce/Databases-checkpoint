// Definition:

// SQL (Structured Query Language) databases are relational databases that store data in tables with predefined schemas.

// Data is structured using rows and columns, similar to Excel sheets.

// Examples of SQL Databases:

// MySQL

// PostgreSQL

// Microsoft SQL Server

// Oracle

// Best for:

// Applications requiring strict data consistency, such as banking, e-commerce, and enterprise applications.

// Definition:

// MongoDB is a NoSQL database that stores data in flexible, JSON-like documents instead of tables.

// Schema-less design allows storing different structures of data in the same collection.

// Best for:

// Applications that need scalability, flexibility, and fast development, such as real-time analytics, IoT, and social media apps.

// Key Features:
// Document-based storage
// Flexible schema
// Horizontal scalability
